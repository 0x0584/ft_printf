#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	t_uint64 u;

	u = (n < 0) ? -n : n;
	if (n < 0)
		ft_putchar_fd('-', fd);
	if (u > 9)
		ft_putnbr_fd(u / 10, fd);
	ft_putchar_fd(u % 10 + '0', fd);
}
