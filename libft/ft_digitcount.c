#include "libft.h"

t_s8	ft_digitcount(int n)
{
	t_s8	count;
	t_u16	u;

	if (n == 0)
		return (1);
	u = (n < 0) ? -n : n;
	count = 1 + (n < 0);
	while (u / 10)
	{
		u /= 10;
		count++;
	}
	return (count);
}
